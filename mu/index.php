                    <dd>実現可能です。無垢材に限らず、構造は、RC（鉄筋コンクリート造）にするが、中は木造にすることが可能です。</br>
                        耐震性・耐久性にはこだわりたいですが、木のぬくもりや温かい印象のあるお家にしたいというご希望もすべて叶えることができます。
                    </dd>
                </dl>
                <dl>
                    <dt>RC（鉄筋コンクリート造）は寒い印象があるのですが？</dt>
                    <dd>断熱、気密性に関しては、どの建材よりもRC（鉄筋コンクリート造）が一番優れています。</br>
                        鉄筋コンクリートは、隙間がないので外気が入ってこない＝気密性が高いです。気密性が高いと熱負荷が少なくなるので、断熱性にも優れております。また熱吸収が少ないので、エアコンなどの効率もよく快適な空間となります。
                    </dd>
                </dl>
